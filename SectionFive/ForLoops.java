public class ForLoops {
  public static void main(String[] args) {
    for (int i = 1; i <= 3; i++) {
      System.out.println(i);
    }

    System.out.print("Incoming conuters: ");
    for (int i = 0; i<=4; i++){
      System.out.println(i + " ");
    }

    for (int i = 10; i>4; i--){
      System.out.println(i + " ");
    }
  }
}